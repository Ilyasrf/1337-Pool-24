#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = 1;
	while (i < ac)
	{
		j = 0;
		while (av[ac - i][j])
		{
			write (1, &av[ac - i][j], 1);
			j++;
		}
		write (1, "\n", 1);
		i++;
	}
}
